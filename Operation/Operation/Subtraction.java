package Lesson_7.Operation.Operation;

public class Subtraction extends Operands{

    @Override
    public void getNum1(int num1) {
        
        super.getNum1(num1);
    }

    @Override
    public void getNum2(int num2) {
       
        super.getNum2(num2);
    }

    @Override
    public Integer result() {
       
        return num1 - num2;
    }

    
    
}
